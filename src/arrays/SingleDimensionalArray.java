package arrays;

public class SingleDimensionalArray {

	public static void main(String[] args) {
		
		String names[]=new String[5];
		   names[0]="kumar";
		   names[1]="sai";
		   names[2]="prasanna";
		   names[3]="teja";
		   names[4]="raja";
		   
		   System.out.println(names.length);
		   
		  for (int i = 0; i <= names.length; i++) {
			System.out.println(names[i]);
		} 
		   
		   
		
	}

}
