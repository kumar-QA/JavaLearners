
public class LogicalOperators {

	public static void main(String[] args) {
	
		int a=10;
		int b=20;
		int c=30;
		int d=40;
		
		if(a>5 && a>b) {
			System.out.println("true");
		}else {
			System.out.println("false");
		}
		
		if(a>5 && a<b) {
			System.out.println("true");
		}else {
			System.out.println("false");
		}
		
		if(a>5 || a>b) {
			System.out.println("true");
		}else {
			System.out.println("false");
		}
		
		if(a<5 || a>b) {
			System.out.println("true");
		}else {
			System.out.println("false");
		}
		
				
		
		
		
		
	}

}
